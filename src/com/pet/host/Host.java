package com.pet.host;

public class Host {
	private int hostNum, pay, max_pet;
	private String address, search_on, rule, facility_info, subject, content, created;
	
	public int getHostNum() {
		return hostNum;
	}
	public void setHostNum(int hostNum) {
		this.hostNum = hostNum;
	}
	public int getPay() {
		return pay;
	}
	public void setPay(int pay) {
		this.pay = pay;
	}
	public int getMax_pet() {
		return max_pet;
	}
	public void setMax_pet(int max_pet) {
		this.max_pet = max_pet;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getSearch_on() {
		return search_on;
	}
	public void setSearch_on(String search_on) {
		this.search_on = search_on;
	}
	public String getRule() {
		return rule;
	}
	public void setRule(String rule) {
		this.rule = rule;
	}
	public String getFacility_info() {
		return facility_info;
	}
	public void setFacility_info(String facility_info) {
		this.facility_info = facility_info;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getCreated() {
		return created;
	}
	public void setCreated(String created) {
		this.created = created;
	}
	
}
